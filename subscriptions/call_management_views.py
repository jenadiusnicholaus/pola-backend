"""
Call Management Views for Incoming Calls via FCM
Handles call initiation, acceptance, rejection, ending, and status tracking
"""

from rest_framework import viewsets, status
from rest_framework.decorators import action
from rest_framework.response import Response
from rest_framework.permissions import IsAuthenticated
from django.db import transaction
from django.utils import timezone
from django.shortcuts import get_object_or_404
import logging

from .models import CallSession, UserCallCredit
from authentication.models import PolaUser
from authentication.device_models import UserDevice
from notification.models import UserOnlineStatus
from notification.google_firebase_service.push_notification.auth_api import GoogleAuth
from notification.google_firebase_service.push_notification.fcm_api import FCM

logger = logging.getLogger(__name__)


class CallManagementViewSet(viewsets.ViewSet):
    """
    ViewSet for managing incoming calls with FCM notifications
    
    Endpoints:
    - POST /calls/initiate/ - Initiate a call to a consultant
    - POST /calls/{id}/accept/ - Accept an incoming call
    - POST /calls/{id}/reject/ - Reject an incoming call
    - POST /calls/{id}/end/ - End an active call
    - POST /calls/{id}/missed/ - Mark call as missed
    - GET /calls/consultants/{consultant_id}/status/ - Check consultant online status
    """
    permission_classes = [IsAuthenticated]
    
