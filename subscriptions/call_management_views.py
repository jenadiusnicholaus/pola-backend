        logger.info("⚠️ BYPASSING online status check for testing")
        # try:
        #     consultant_status = UserOnlineStatus.objects.get(user=consultant)
        #     if not consultant_status.is_available_for_call():
        #         status_message = {
        #             'offline': 'Consultant is currently offline',
        #             'busy': 'Consultant is currently on another call'
        #         }.get(consultant_status.status, 'Consultant is unavailable')
        #         
        #         return Response({
        #             'error': 'consultant_unavailable',
        #             'message': status_message,
        #             'consultant_status': consultant_status.status
        #         }, status=status.HTTP_503_SERVICE_UNAVAILABLE)
        # except UserOnlineStatus.DoesNotExist:
        #     # Auto-create online status for consultants without one (for development/testing)
        #     logger.warning(f"⚠️ Consultant {consultant.email} has no UserOnlineStatus. Creating one as 'available'.")
        #     try:
        #         consultant_status = UserOnlineStatus.objects.create(
        #             user=consultant,
        #             is_online=True,
        #             status='available',
        #             last_heartbeat=timezone.now()
        #         )
        #     except Exception as e:
        #         # If creating UserOnlineStatus fails (e.g., location API timeout), log and continue
        #         logger.error(f"❌ Failed to create UserOnlineStatus: {str(e)}")
        #         # Continue without status check for this call
        #         pass
